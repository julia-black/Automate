public class Pair {
    boolean res;
    int n;

    public Pair(boolean res, int n) {
        this.res = res;
        this.n = n;
    }

    public boolean isRes() {
        return res;
    }

    public void setRes(boolean res) {
        this.res = res;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }
}